package com.gappu.staticexample;

public class Human {
    int age;
    String name;
    boolean isMarried;
    int salary;

    public Human(int age, String name, boolean isMarried, int salary) {
        this.age = age;
        this.name = name;
        this.isMarried = isMarried;
        this.salary = salary;
    }
}
